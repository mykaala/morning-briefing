def get_quran_verse():
    """Fetch a Quran verse using the alquran.cloud API."""
    today = datetime.date.today()
    rng = random.Random(today.toordinal())
    verse_number = rng.choice(CURATED_VERSES)

    url = f"https://api.alquran.cloud/v1/ayah/{verse_number}/editions/quran-uthmani,en.asad"

    response = requests.get(url)
    response.raise_for_status()

    data = response.json()
    # When requesting multiple editions, data["data"] is a list — one object per edition
    editions = data["data"]

    arabic_text = None
    english_text = None
    surah_number = None
    surah_name = None
    ayah_number = None

    for verse in editions:
        identifier = verse["edition"]["identifier"]
        if identifier == "quran-uthmani":
            arabic_text = verse["text"]
            surah_number = verse["surah"]["number"]
            surah_name = verse["surah"]["englishName"]
            ayah_number = verse["numberInSurah"]
        elif identifier == "en.asad":
            english_text = verse["text"]

    return {
        "arabic": arabic_text,
        "translation": english_text,
        "surah_name": surah_name,
        "ayah_number": ayah_number,
        "surah_number": surah_number,
    }


if __name__ == "__main__":
    result = get_quran_verse()
    print(result)
